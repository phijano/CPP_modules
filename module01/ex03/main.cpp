#include "Weapon.hpp"
#include "HumanA.hpp"
#include "HumanB.hpp"
#include <stdlib.h>

void leaks(void)
{
	system("leaks Violenciainnecesaria");
}

int main()
{
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	atexit(leaks);
	return 0;
}
